import math
an = float(input('Digite o valor do ângulo: '))
se = math.sin(math.radians(an))
co = math.cos(math.radians(an))
ta = math.tan(math.radians(an))
print('Ângulo de {} tem a SENO: {:.2f}'.format(an,se))
print('Angulo de {} tem o COSSENO: {:.2f}'.format(an,co))
print('Anfulo de {} tem a TANGENTE: {:.2f}'.format(an,ta))





