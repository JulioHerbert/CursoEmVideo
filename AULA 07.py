n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))

so = n1+n2
mu = n1*n2
su = n1-n2
di = n1/n2
po = n1**n2
dt = n1//n2
re = n1%n2

print('Soma: {}\nMultiplicação: {}\nSubtração: {}\nDivisão: {}\nPotência: {}'.format(so,mu,su,di,po))
print('Divisão inteira: {}\nResto da Visão: {}'.format(dt,re))
